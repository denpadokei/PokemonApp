using PokemonApp.AbilityValueConverter.DataBases;
using PokemonApp.Core.Collections;
using PokemonApp.DataBase.Models;
using Prism.Mvvm;
using System.Collections.ObjectModel;

namespace PokemonApp.AbilityValueConverter.Models
{
    public class AbilityValueConverterDomain : BindableBase
    {
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プロパティ
        /// <summary>ポケモンの名前コレクション を取得、設定</summary>
        private MTObservableCollection<string> pokemonNames_;
        /// <summary>ポケモンの名前コレクション を取得、設定</summary>
        public MTObservableCollection<string> PokemonNames
        {
            get { return this.pokemonNames_; }
            set { this.SetProperty(ref pokemonNames_, value); }
        }

        /// <summary>個体値推定 を取得、設定</summary>
        private MTObservableCollection<IndividualEntity> individuals_;
        /// <summary>個体値推定 を取得、設定</summary>
        public MTObservableCollection<IndividualEntity> Individuals
        {
            get { return this.individuals_; }
            set { this.SetProperty(ref individuals_, value); }
        }

        /// <summary>個体値一覧 を取得、設定</summary>
        private MTObservableCollection<AbilityEntity> abilityes_;
        /// <summary>個体値一覧 を取得、設定</summary>
        public MTObservableCollection<AbilityEntity> Abilityes
        {
            get { return this.abilityes_; }
            set { this.SetProperty(ref abilityes_, value); }
        }

        /// <summary>選択中のポケモン を取得、設定</summary>
        private string currentPokemon_;
        /// <summary>選択中のポケモン を取得、設定</summary>
        public string CurrentPokemon
        {
            get { return this.currentPokemon_; }
            set { this.SetProperty(ref currentPokemon_, value); }
        }

        /// <summary>計算対象のポケモン を取得、設定</summary>
        private pokemon pokemon_;
        /// <summary>計算対象のポケモン を取得、設定</summary>
        public pokemon Pokemon
        {
            get { return this.pokemon_; }
            set { this.SetProperty(ref pokemon_, value); }
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // コマンド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // コマンド用メソッド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // リクエスト
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // オーバーライドメソッド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // プライベートメソッド
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // パブリックメソッド
        public void Serch()
        {
            this.PokemonNames.Clear();
            using (var repository = new Repository()) {
                this.PokemonNames.AddRange(AbilityValueConverterDataBase.FindPokemon(repository.Context));
            }
        }

        public void Calculation()
        {
            this.Abilityes.Clear();
            this.Individuals.Clear();

            var hp = 0;
            var attack = 0;
            var block = 0;
            var contact = 0;
            var defence = 0;
            var speed = 0;




            this.Individuals = new MTObservableCollection<IndividualEntity>()
            {
                new IndividualEntity("HP", hp),
                new IndividualEntity("こうげき", attack),
                new IndividualEntity("ぼうぎょ", block),
                new IndividualEntity("とくこう", contact),
                new IndividualEntity("とくぼう", defence),
                new IndividualEntity("すばやさ", speed),
            };
        }
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // メンバ変数
        #endregion
        //ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*ﾟ+｡｡+ﾟ*｡+ﾟ ﾟ+｡*
        #region // 構築・破棄
        public AbilityValueConverterDomain()
        {
            this.PokemonNames = new MTObservableCollection<string>();
            this.Individuals = new MTObservableCollection<IndividualEntity>();
            this.Abilityes = new MTObservableCollection<AbilityEntity>();
        }
        #endregion
    }
}
