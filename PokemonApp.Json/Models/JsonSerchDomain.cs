        public void Serch()
        {
            this.Collection.AddRange(JsonData.FindTrick());
        }

        public bool Regist()
        {
            using (var repository = new Repository()) {
                foreach (var trick in this.Collection) {
                    var updateTrick = repository.Context.tricks.FirstOrDefault(x => x.trick_name == trick.JName);
                    var type = repository.Context.types.FirstOrDefault(x => x.eng_type_name == trick.Type);
                    var category = repository.Context.categories.FirstOrDefault(x => x.c_category_name == trick.Category);
                    if (updateTrick == null) {

                        repository.Context.Add(new trick() {
                            trick_name = trick.JName,
                            power = trick.Power,
                            accuracy_rate = trick.Accuracy,
                            type_id =  type != null ? type.type_id : 0,
                            attribute = category != null ? category.category_id : 0,
                            pp = trick.Pp
                        });
                    }
                    else {
                        updateTrick.power = trick.Power;
                        updateTrick.accuracy_rate = trick.Accuracy;
                        updateTrick.type_id = type != null ? type.type_id : 0;
                        updateTrick.attribute = category != null ? category.category_id : 0;
                        updateTrick.pp = trick.Pp;
                    }
                }

                repository.Context.SaveChanges();
                return true;
            }
        }
