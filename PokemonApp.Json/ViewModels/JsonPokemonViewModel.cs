        private DelegateCommand registCommand_;
        /// <summary>登録コマンド を取得、設定</summary>
        public DelegateCommand RegistCommand { get { return this.registCommand_ ?? (this.registCommand_ = new DelegateCommand(this.Regist)); } }
        #endregion
